#include <stdio.h>

int main(){
   
    int hexToDec;

    scanf("%x",&hexToDec);

    printf("hex to decs - %d\n",hexToDec);

    
    
    int decToHex;

    scanf("%d",&decToHex);

    printf("decs to hex - %x\n",decToHex);


    int octToDec;

    scanf("%o",&octToDec);

    printf("oct to decs - %d\n",octToDec);

   
    int decToOct;

    scanf("%d",&decToOct);

    printf("decs to oct - %o\n",decToOct);


    int hexToOct;

    scanf("%x",&hexToOct);

    printf("hex to oct - %o\n",hexToOct);


    int octToHex;

    scanf("%o",&octToHex);

    printf("oct to hex - %x\n",octToHex);


}